class Operadores:

    def __init__(self):
        pass

    def suma(self, num1, num2):
        resultado = num1 + num2
        return resultado
    
    def resta(self, num1, num2):
        resultado = num1 - num2
        return resultado
    
    def multiplicacion(self, num1, num2):
        resultado = num1 * num2
        return resultado
    
    def division(self, num1, num2):
        resultado = num1 / num2
        return resultado



